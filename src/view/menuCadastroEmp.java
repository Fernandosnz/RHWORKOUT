    private void EmpPesquisaIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EmpPesquisaIDActionPerformed

//BOTÃO PESQUISA ID 
            int idPesquisa = Integer.parseInt(CampoiDPesquisa.getText());
                EmpresaDAO empresaDAO = new EmpresaDAO();
                Empresa empresa = empresaDAO.getEmpresa(idPesquisa);
                if(empresa == null){
                  JOptionPane.showMessageDialog(this,"Empresa não encontrada :(");
                  
                }
                
                else{
                    campoIDemp.setText(String.valueOf(empresa.getId()));
                    jTextFieldCadastroNomeEmp.setText(empresa.getNome());
                    jTextFieldCadastroEndEmp.setText(empresa.getEndereco());
                    jTextFieldCadastroEmailEmp.setText(empresa.getEmail());
                    jTextFieldCadastroCNPJEmp.setText(empresa.getCNPJ());
                    
                    
                }
                
                
                
                
                
    }//GEN-LAST:event_EmpPesquisaIDActionPerformed

