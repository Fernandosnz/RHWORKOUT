    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(campoIDemp.getText());
        String nomeEmpresa = jTextFieldCadastroNomeEmp.getText();
        String endEmpresa = jTextFieldCadastroEndEmp.getText();
        String emailEmpresa = jTextFieldCadastroEmailEmp.getText();
        String CNPJEmpresa = jTextFieldCadastroCNPJEmp.getText();
        
        Empresa empresa = new Empresa ();
        empresa.setId(id);
        empresa.setNome(nomeEmpresa);
        empresa.setEndereco(endEmpresa);
        empresa.setEmail(emailEmpresa);
        empresa.setCNPJ(CNPJEmpresa);
        
        EmpresaDAO empresaDAO = new EmpresaDAO();
        empresaDAO.editar(empresa);
        
        campoIDemp.setText("");
        jTextFieldCadastroNomeEmp.setText("");
        jTextFieldCadastroEndEmp.setText("");
        jTextFieldCadastroEmailEmp.setText("");
        jTextFieldCadastroCNPJEmp.setText("");
        
    }//GEN-LAST:event_jButtonEditarActionPerformed

